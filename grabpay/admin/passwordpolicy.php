<b>1. Overview </b><br>
Passwords are an important aspect of computer security. A poorly chosen password may result in unauthorized access and/or exploitation of 's resources. All users, including merchant and agent with access to systems, are responsible for taking the appropriate steps, as outlined below, to select and secure their passwords. <br><br>
<b>2. Purpose </b><br>
The purpose of this policy is to establish a standard for creation of strong passwords, the protection of those passwords, and the frequency of change. <br><br>
<b>3. Scope </b><br>
The scope of this policy includes all personnel who have or are responsible for an account (or any form of access that supports or requires a password) on any system that resides at any facility, has access to the network, or stores any nonpublic information.<br><br> 
<b>4. Policy</b><br>
<b>4.1 Password Creation</b><br>
<b>4.1.1 </b>All user-level and system-level passwords must conform to the Password Construction Guidelines. <br>
<b>4.1.2 </b>Users must not use the same password for accounts as for other non access (for example, personal ISP account, option trading, benefits, and so on).<br> 
<b>4.1.3 </b>Where possible, users must not use the same password for various access needs.<br>
<b>4.1.4 </b>User accounts that have system-level privileges granted through group memberships or programs such as sudo must have a unique password from all other accounts held by that user to access system-level privileges. <br>
<b>4.1.5 </b>Where Simple Network Management Protocol (SNMP) is used, the community strings must be defined as something other than the standard defaults of public, private, and system and must be different from the passwords used to log in interactively. SNMP community strings must meet password construction guidelines.<br><br> 
<b>4.2 Password Change </b><br>
<b>4.2.1 </b>All system-level passwords (for example, root, enable, NT admin, application administration accounts, and so on) must be changed on at least a quarterly basis. <br>
<b>4.2.2 </b>All user-level passwords (for example, email, web, desktop computer, and so on) must be changed at least every six months. The recommended change interval is every four months. <br>
<b>4.2.3 </b>Password cracking or guessing may be performed on a periodic or random basis by the Infosec Team or its delegates. If a password is guessed or cracked during one of these scans, the user will be required to change it to be in compliance with the Password Construction Guidelines. <br><br>
<b>4.3 Password Protection </b><br>
<b>4.3.1 </b>Passwords must not be shared with anyone. All passwords are to be treated as sensitive, Confidential information. Corporate Information Security recognizes that legacy applications do not support proxy systems in place. Please refer to the technical reference for additional details. <br>
<b>4.3.2</b> Passwords must not be inserted into email messages, Alliance cases or other forms of electronic communication.<br>
<b>4.3.3</b> Passwords must not be revealed over the phone to anyone. <br>
<b>4.3.4</b> Do not share passwords with anyone, including administrative assistants, secretaries, managers, co-workers while on vacation, and family members. <br>
<b>4.3.5</b> Do not write passwords down and store them anywhere in your office. Do not store passwords in a file on a computer system or mobile devices (phone, tablet) without encryption. <br>
<b>4.3.6</b> Do not use the "Remember Password" feature of applications (for example, web browsers). <br>
<b>4.3.7</b> Any user suspecting that his/her password may have been compromised must report the incident and change all passwords. <br><br>
<b>4.4 Application Development Application developers must ensure that their programs contain the following security precautions:  </b><br>
<b>4.4.1</b> Applications must support authentication of individual users, not groups. <br>
<b>4.4.2 </b>Applications must not store passwords in clear text or in any easily reversible form. <br>
<b>4.4.3</b> Applications must not transmit passwords in clear text over the network. <br>
<b>4.4.4 </b>Applications must provide for some sort of role management, such that one user can take over the functions of another without having to know the other's password. <br><br>
<b>4.5 Use of Passwords and Passphrases </b>
Passphrases are generally used for public/private key authentication. A public/private key system defines a mathematical relationship between the public key that is known by all, and the private key, that is known only to the user. Without the passphrase to "unlock" the private key, the user cannot gain access. Passphrases are not the same as passwords. A passphrase is a longer version of a password and is, therefore, more secure. A passphrase is typically composed of multiple words. Because of this, a passphrase is more secure against "dictionary attacks." A good passphrase is relatively long and contains a combination of upper and lowercase letters and numeric and punctuation characters. 
An example of a good passphrase: "The*?#>*@TrafficOnThe101Was*&#!#ThisMorning" 
All of the rules above that apply to passwords apply to passphrases. <br><br>
<b>5. Policy Compliance </b><br>
<b>5.1 Compliance Measurement</b> <br>
The Infosec team will verify compliance to this policy through various methods, including but not limited to, periodic walk-thrus, video monitoring, business tool reports, internal and external audits, and feedback to the policy owner. <br><br>
<b>5.2 Exceptions </b><br>
Any exception to the policy must be approved by the Infosec Team in advance. <br><br>
<b>5.3 Non-Compliance </b><br>
An employee found to have violated this policy may be subject to disciplinary action, up to and including termination of employment. 
